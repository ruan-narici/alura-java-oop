
public class TestaVariaveis {
	public static void main(String[] args) {
		int idadeFilho;
		int idadeMae;
		
		idadeFilho = 24;
		System.out.println("A idade de Ruan é: " + idadeFilho + " anos.");
		idadeMae = idadeFilho + 20;
		System.out.println("A idade da Mãe de Ruan é: "+ idadeMae + " anos.");
		System.out.println("Sendo assim, a Mãe de Ruan é 20 anos mais velha do que ele.");
	}
}
