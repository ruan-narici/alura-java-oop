
public class TestaPontoFlutuante {
	public static void main (String[] args) {
		double salario = 1250.70;
		int divisaoInt = 5 / 2;
		double divisaoDouble = 5 / 2;
		double divisaoDoubleCorrect = 5.0 / 2.0;
		
		System.out.println("Meu salário é: " + salario);
		System.out.println("** 5 / 2 **");
		System.out.println("Divisão de inteiro: " + divisaoInt);
		System.out.println("Divisão de double inteiro: " + divisaoDouble);
		System.out.println("Divisão de double decimal: " + divisaoDoubleCorrect);
	}
}
